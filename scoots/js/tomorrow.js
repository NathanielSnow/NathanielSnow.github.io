var d = new Date();
var tomorrow = d.setDate(d.getDate() + 1);

function formatDate(date) {
    var d = new Date(date),
        month = '' + (d.getMonth() + 1),
        day = '' + d.getDate(),
        year = d.getFullYear();

    if (month.length < 2) 
        month = '0' + month;
    if (day.length < 2) 
        day = '0' + day;

    return [year, month, day].join('-');
}



document.getElementById("rentalDate").setAttribute('min', formatDate(tomorrow));